#!/usr/bin/env bash
if ! [ -L /var/www ]; then
  sudo rm -rf /var/www
  sudo ln -fs /vagrant /var/www
fi

set -e
echo "*****************************************************************************************"
echo "******************************* Install NGINX  ******************************************"
echo "*****************************************************************************************"

apt-get update
apt-get install -y nginx
chown vagrant:root -R /var/www/html/

echo "*****************************************************************************************"
echo "********************************* NGINX is ready  ****************************************"
echo "**********************************http://192.168.99.11/index.html*************************"
echo "*****************************************************************************************"
service nginx restart





